    uri = os.environ.get("NEO4J_URI", "")
    username = os.environ.get("NEO4J_USERNAME", "")
    password = os.environ.get("NEO4J_PASSWORD", "")

    if not all([uri, username, password]):
        pytest.skip("NEO4J_URI, NEO4J_USERNAME, NEO4J_PASSWORD required in .env")

    hostname = uri
    for scheme in ("neo4j+s://", "bolt+s://", "neo4j://", "bolt://"):
        if hostname.startswith(scheme):
            hostname = hostname[len(scheme):]
            break

    return {"hostname": hostname.rstrip("/"), "username": username, "password": password}


