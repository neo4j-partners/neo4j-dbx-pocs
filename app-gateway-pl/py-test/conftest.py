"""
Session-scoped fixtures for Aura BC Application Gateway connectivity tests.

Self-contained — reads .env generated by deploy_test_vm.py (SCP'd to the VM).
Aura API functions are inlined so no parent directory imports are needed.
"""

import os

import pytest
import requests
from dotenv import load_dotenv

load_dotenv()

# ---------------------------------------------------------------------------
# Aura API helpers (inlined — no imports from parent project)
# ---------------------------------------------------------------------------
API_BASE = "https://api.neo4j.io/v2beta1"
TOKEN_URL = "https://api.neo4j.io/oauth/token"


def _get_token():
    resp = requests.post(
        TOKEN_URL,
        data={"grant_type": "client_credentials"},
        auth=(os.environ["AURA_API_CLIENT_ID"], os.environ["AURA_API_CLIENT_SECRET"]),
        timeout=45,
    )
    resp.raise_for_status()
    return resp.json()["access_token"]


def _auth_headers():
    return {"Authorization": f"Bearer {_get_token()}", "Content-Type": "application/json"}


def _list_filters():
    org_id = os.environ["AURA_ORG_ID"]
    resp = requests.get(f"{API_BASE}/organizations/{org_id}/ip-filters", headers=_auth_headers(), timeout=45)
    resp.raise_for_status()
    body = resp.json()
    return body if isinstance(body, list) else body.get("data", [])


def _find_filter_for_ip(ip_address):
    cidr = f"{ip_address}/32"
    for f in _list_filters():
        for entry in f.get("allow_list", []):
            if entry.get("ip_range", "") in (cidr, ip_address):
                return f["id"]
    return None


def _add_ip(ip_address, description):
    org_id = os.environ["AURA_ORG_ID"]
    instance_id = os.environ["AURA_INSTANCE_ID"]
    cidr = ip_address if "/" in ip_address else f"{ip_address}/32"
    payload = {
        "name": f"AppGW-Test-{ip_address}"[:30],
        "organization_id": org_id,
        "allow_list": [{"ip_range": cidr, "description": description}],
        "filtered_entities": {"instances": [instance_id], "projects": [], "organizations": []},
    }
    resp = requests.post(
        f"{API_BASE}/organizations/{org_id}/ip-filters",
        headers=_auth_headers(), json=payload, timeout=45,
    )
    resp.raise_for_status()
    result = resp.json()
    data = result.get("data", result) if isinstance(result, dict) else result
    return data.get("id", "unknown") if isinstance(data, dict) else "unknown"


def _remove_filter(filter_id):
    org_id = os.environ["AURA_ORG_ID"]
    resp = requests.delete(
        f"{API_BASE}/organizations/{org_id}/ip-filters/{filter_id}",
        headers=_auth_headers(), timeout=45,
    )
    resp.raise_for_status()


